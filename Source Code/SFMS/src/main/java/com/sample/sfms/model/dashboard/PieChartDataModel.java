package com.sample.sfms.model.dashboard;

public class PieChartDataModel {
    private String name;
    private int value;

    public PieChartDataModel() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

}
